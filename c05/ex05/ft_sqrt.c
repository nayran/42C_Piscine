int		ft_sqrt(int nb)
{
	long int	result;
	long int	x;
	int			y;

	result = 1;
	x = 1;
	while (result <= nb)
	{
		result = x * x;
		if (result == nb)
		{
			y = x;
			return (y);
		}
		x++;
	}
	return (0);
}
