#include <unistd.h>

int		main(int argc, char *argv[])
{
	int x;
	int y;

	x = 1;
	y = 0;
	while (x < argc)
	{
		while (argv[x][y] != '\0')
		{
			write(1, &argv[x][y], 1);
			y++;
		}
		x++;
		y = 0;
		write(1, "\n", 1);
	}
}
