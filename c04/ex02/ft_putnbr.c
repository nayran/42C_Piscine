#include <unistd.h>

void	ft_putnbr(int nb)
{
	long x;

	x = nb;
	if (x < 0)
	{
		write(1, "-", 1);
		x = x * -1;
	}
	if ((x / 10) > 0)
		ft_putnbr(x / 10);
	x = x % 10 + 48;
	write(1, &x, 1);
}
