#include <unistd.h>

void	print(int *a)
{
	char i;

	write(1, a, 1);
	i = *a;
	a++;
	write(1, a, 1);
	a++;
	if (i != '7')
	{
		write(1, a, 1);
		write(1, ", ", 2);
	}
	else
	{
		write(1, a, 1);
	}
}

void	ft_print_comb(void)
{
	int a[3];

	a[0] = 48;
	a[1] = 48;
	a[2] = 48;
	while (a[0] < 58)
	{
		while (a[1] < 58)
		{
			while (a[2] < 58)
			{
				if (a[0] < a[1] && a[1] < a[2])
				{
					print(&a[0]);
				}
				a[2]++;
			}
			a[2] = 48;
			a[1]++;
		}
		a[1] = 48;
		a[0]++;
	}
}
