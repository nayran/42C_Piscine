#include <unistd.h>

int		main(int argc, char **argv)
{
	char	*c;
	int		x;

	c = argv[0];
	x = argc;
	while (*c != '\0')
	{
		write(1, c, 1);
		c++;
	}
	write(1, "\n", 1);
	return (0);
}
